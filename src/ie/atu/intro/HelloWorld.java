package ie.atu.intro;

/* ***************************************************************************
 * The keyword public is know as a modifier. 
 * 
 * public is a Java keyword which declares a member's access as public. 
 * public members are visible to all other classes. 
 * This means that any other class can access a public field or method. 
 * 
 * The keyword class begins the class definition for a class named HelloWorld
 * ***************************************************************************/

public class HelloWorld {    
	/* In the Java programming language, every application must contain a 
	 * main method whose signature is: public static void main(String[] args)
	 * It's the entry point for your application and will subsequently invoke 
	 * all the other methods required by your program. The main method accepts 
	 * a single argument: an array of elements of type String
	 */
	public static void main(String[] args) {
		System.out.println("Hello, World!");
	
	}
	
}
